def twenty_twenty_two():
    """Come up with the most creative expression that evaluates to 2022,
    using only numbers and the +, *, and - operators.

    Expected result:
    >>> twenty_twenty_two()
    2022
    """
    return 2021+1


def sum(a, b):
    """Compute the sum of a and b.

    Expected result:
    >>> sum(1, 2)
    3
    >>> sum(3, 8)
    11
    """
    return a+b


def diff_square(a, b):
    """Compute the difference of square a and square b.

    Expected result:
    >>> diff_square(3, 2)
    5
    >>> diff_square(3, 4)
    -7
    """
    return a*a-b*b
